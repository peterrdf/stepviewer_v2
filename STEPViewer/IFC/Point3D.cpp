#include "stdafx.h"
#include "Point3D.h"

// ------------------------------------------------------------------------------------------------
CPoint3D::CPoint3D(double dX, double dY, double dZ)
	: m_dX(dX)
	, m_dY(dY)
	, m_dZ(dZ)
{
}

// ------------------------------------------------------------------------------------------------
CPoint3D::CPoint3D()
	: CPoint3D(0., 0., 0.)
{
}

// ------------------------------------------------------------------------------------------------
double& CPoint3D::x()
{
	return m_dX;
}

// ------------------------------------------------------------------------------------------------
double& CPoint3D::y()
{
	return m_dY;
}

// ------------------------------------------------------------------------------------------------
double& CPoint3D::z()
{
	return m_dZ;
}